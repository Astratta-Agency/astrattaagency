/** Renders a single `<script type="application/ld+json">` block. Use for every schema object instead of hand-rolling script tags. */
export function JsonLd({ data }: { data: object }) {
  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(data).replace(/</g, '\\u003c') }}
    />
  )
}
